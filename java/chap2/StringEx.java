public class StringEx {
  public static void main(String[] args) {
    String name = "Ja" + "va";
    String ex = name + 8.0;
    
    System.out.println(name);
    System.out.println(ex);
    System.out.println(7 + " ");
    System.out.println(" " + 7);
    System.out.println(7 + "");
    System.out.println("" + 7);
    System.out.println("" + "");
    System.out.println(7 + 7 + "");
    System.out.println("" + 7 + 7);
  }
}
